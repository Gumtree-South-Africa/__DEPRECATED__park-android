package com.ebay.park.responses;

public class ProfileImgResponse {

	private String url;
	private String name;

	public ProfileImgResponse() {
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public String getName() {
		return name;
	}
}
